#include<iostream>
using namespace std;
int main(){

    // ++ Increase the value by one, -- decreases value(Post Incrementation)

    int a=12, b= 11; 
    cout << a+b; 
    cout << endl; 
    a++,b++; 
    cout << a+b<< endl ;

    a=15,b=20; 
    cout << a+b << endl; 
    ++a,++b; 
    cout << a+b; 

}